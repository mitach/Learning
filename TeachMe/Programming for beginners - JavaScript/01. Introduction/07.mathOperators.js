function mathOperators(numOne, numTwo) {
    console.log(`Sum: ${Math.abs(numOne + numTwo)}`);
    console.log(`Difference: ${Math.abs(numOne - numTwo)}`);
    console.log(`Product: ${Math.abs(numOne * numTwo)}`);
    console.log(`Average: ${Math.abs((numOne + numTwo) / 2)}`);

}

mathOperators(25, 5)